SELECT 
SOURCEAIRPORTID, 
DESTAIRPORTID, 
SOURCEAIRPORT, 
DESTAIRPORT, 
AIRLINES.AIRLINEID as AIRLINEID, 
AIRLINES.NAME AS AIRLINENAME, 
STOPS, 
ACTIVE 
FROM ROUTES, AIRLINES 
WHERE SOURCEAIRPORTID = '3339' 
AND DESTAIRPORTID = '3361' 
AND ACTIVE = 'Y' 
ORDER BY STOPS ASC
LIMIT 100;

